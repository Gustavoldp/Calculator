public class FourOperations {

    public static int somador(int x, int y){
        return x + y;
    }

    public static int subtrador(int x, int y){
        return x - y;
    }

    static int muiltiplicador(int x, int y) {
        return x * y;
    }

    public static int divisor(int x, int y){
        return x / y;
    }
}
